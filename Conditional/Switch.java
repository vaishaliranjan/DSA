public class Switch {
    public static void main(String args[]){
        char ch='d';
        switch(ch){
            case 'c':
            System.out.println("Chips");
            break;

            case 'd':
            System.out.println("Dairy");
            break;

            case 'a':
            System.out.println("Apple");
            break;

            default:
            System.out.println("Default");


        }
    }
}
